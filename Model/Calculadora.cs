namespace Exec1.Model
{
    public class Calculadora
    {
        public static double Somar(int a, int b) => a + b;

        public static double Dividir(int a, int b) => a / b;

        public static double Multiplicar(int a, int b) => a * b;

        public static double Subtrair(int a, int b) => a - b;
    }
}